package Model;

public class TrainStationTimeDTO {
	private String stationTimeCode;
	private String stationCode;
	private int trainCode;
	private String time;
	public String getStationTimeCode() {
		return stationTimeCode;
	}
	public void setStationTimeCode(String stationTimeCode) {
		this.stationTimeCode = stationTimeCode;
	}
	public String getStationCode() {
		return stationCode;
	}
	public void setStationCode(String stationCode) {
		this.stationCode = stationCode;
	}
	public int getTrainCode() {
		return trainCode;
	}
	public void setTrainCode(int trainCode) {
		this.trainCode = trainCode;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
