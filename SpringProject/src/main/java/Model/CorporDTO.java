package Model;

public class CorporDTO {
	private String corporId;
	private String corporPw;
	private String corporName;
	private String corporEmail;
	private String corporPhone;
	private String corporAddr;
	public String getCorporId() {
		return corporId;
	}
	public void setCorporId(String corporId) {
		this.corporId = corporId;
	}
	public String getCorporPw() {
		return corporPw;
	}
	public void setCorporPw(String corporPw) {
		this.corporPw = corporPw;
	}
	public String getCorporName() {
		return corporName;
	}
	public void setCorporName(String corporName) {
		this.corporName = corporName;
	}
	public String getCorporEmail() {
		return corporEmail;
	}
	public void setCorporEmail(String corporEmail) {
		this.corporEmail = corporEmail;
	}
	public String getCorporPhone() {
		return corporPhone;
	}
	public void setCorporPhone(String corporPhone) {
		this.corporPhone = corporPhone;
	}
	public String getCorporAddr() {
		return corporAddr;
	}
	public void setCorporAddr(String corporAddr) {
		this.corporAddr = corporAddr;
	}
	
	
}
