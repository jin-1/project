package Model;

public class EventDTO {
   private String ElectedCode;
   private String EvnetCode;
   private String EvenName;
   private int EventQuant;
   private String MemberId;
   private String ExpriyDate;
   public String getElectedCode() {
      return ElectedCode;
   }
   public void setElectedCode(String electedCode) {
      ElectedCode = electedCode;
   }
   public String getEvnetCode() {
      return EvnetCode;
   }
   public void setEvnetCode(String evnetCode) {
      EvnetCode = evnetCode;
   }
   public String getEvenName() {
      return EvenName;
   }
   public void setEvenName(String evenName) {
      EvenName = evenName;
   }
   public int getEventQuant() {
      return EventQuant;
   }
   public void setEventQuant(int eventQuant) {
      EventQuant = eventQuant;
   }
   public String getMemberId() {
      return MemberId;
   }
   public void setMemberId(String memberId) {
      MemberId = memberId;
   }
   public String getExpriyDate() {
      return ExpriyDate;
   }
   public void setExpriyDate(String expriyDate) {
      ExpriyDate = expriyDate;
   }
                   


}